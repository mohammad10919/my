package ir.mohammad.my.model;

public class pishnahad {
    String ima;
    String text1;
    String text2;
    String text3;

    public pishnahad(String ima, String text1, String text2, String text3) {
        this.ima = ima;
        this.text1 = text1;
        this.text2 = text2;
        this.text3 = text3;
    }

    public String getIma() {
        return ima;
    }

    public void setIma(String ima) {
        this.ima = ima;
    }

    public String getText1() {
        return text1;
    }

    public void setText1(String text1) {
        this.text1 = text1;
    }

    public String getText2() {
        return text2;
    }

    public void setText2(String text2) {
        this.text2 = text2;
    }

    public String getText3() {
        return text3;
    }

    public void setText3(String text3) {
        this.text3 = text3;
    }
}
